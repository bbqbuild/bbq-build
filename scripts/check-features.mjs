import { chromium } from 'playwright'
const b = await chromium.launch({ args: ['--use-gl=angle', '--use-angle=swiftshader', '--enable-unsafe-swiftshader'] })
const p = await b.newPage({ viewport: { width: 1720, height: 950 } })
p.setDefaultTimeout(90000)
p.on('dialog', (d) => d.accept())
p.on('pageerror', (e) => console.log('PAGE ERROR:', e.message))
await p.goto('http://127.0.0.1:3000')
await p.evaluate(() => { localStorage.setItem('bbq_view', '3d'); localStorage.removeItem('bbq_fab_pos'); localStorage.removeItem('bbq_chat_pos') })
await p.fill('input[type=password]', process.env.BBQ_USER_PASSWORD)
await p.click('button[type=submit]')
await p.waitForSelector('.topbar')
const fab = await p.$('.chat-fab')
console.log('FAB present:', Boolean(fab))
await p.click('.chat-fab')
await p.waitForTimeout(300)
console.log('window open:', Boolean(await p.$('.chat-window')))
await p.screenshot({ path: 'screenshots/feat-chat.png' })
await p.click('.chat-close')
await p.waitForTimeout(200)
const box = await (await p.$('.chat-fab')).boundingBox()
await p.mouse.move(box.x + 30, box.y + 30)
await p.mouse.down()
await p.mouse.move(1500, 200, { steps: 12 })
await p.mouse.up()
await p.waitForTimeout(200)
const box2 = await (await p.$('.chat-fab')).boundingBox()
console.log('FAB moved to x≈', Math.round(box2.x), 'y≈', Math.round(box2.y))

await p.click('.topbar >> text=Presets')
await p.waitForTimeout(400)
await p.click('text=U-Shape Bar')
await p.waitForTimeout(1200)
await p.keyboard.press('f')
await p.waitForTimeout(500)
await p.evaluate(() => window.__bbq().select({ kind: 'corner', id: 'left' }))
await p.waitForTimeout(300)
const heading = await p.$eval('.panel h2', (e) => e.textContent).catch(() => null)
console.log('panel heading:', heading)
await p.screenshot({ path: 'screenshots/feat-corner.png' })
await p.click('.check-row input')
await p.waitForTimeout(200)
await p.click('.finish-swatch >> nth=2')
await p.waitForTimeout(200)
console.log('corner state:', JSON.stringify(await p.evaluate(() => window.__bbq().design.corners)))
await p.click('text=Remove corner')
await p.waitForTimeout(300)
console.log('after remove:', JSON.stringify(await p.evaluate(() => window.__bbq().design.corners)))
await b.close()
console.log('done')
